@Injectable({
  providedIn: 'root'
})
export class I18nService {
  private readonly storageKey = 'app-language';
  private readonly languageSubject = new BehaviorSubject<AppLanguage>(this.getInitialLanguage());

  readonly language$ = this.languageSubject.asObservable();

  get currentLanguage(): AppLanguage {
    return this.languageSubject.value;
  }

  setLanguage(language: AppLanguage): void {
    if (language === this.currentLanguage) {
      return;
    }

    this.languageSubject.next(language);
    localStorage.setItem(this.storageKey, language);
  }

  t(key: string, params: Record<string, string | number> = {}): string {
    const dictionary = translations[this.currentLanguage];
    const fallbackDictionary = translations.fr;
    const template = dictionary[key] || fallbackDictionary[key] || key;

    return template.replace(/\{(\w+)\}/g, (_, token: string) => {
      const value = params[token];
      return value === undefined || value === null ? '' : String(value);
    });
  }

  tc(key: string, count: number, params: Record<string, string | number> = {}): string {
    const suffix = count > 1 || count === 0 ? 'other' : 'one';
    return this.t(`${key}.${suffix}`, {
      ...params,
      count
    });
  }

  private getInitialLanguage(): AppLanguage {
    const storedLanguage = localStorage.getItem(this.storageKey);
    if (storedLanguage === 'fr' || storedLanguage === 'pt') {
      return storedLanguage;
    }

    const browserLanguage = (navigator.language || '').toLowerCase();
    return browserLanguage.startsWith('pt') ? 'pt' : 'fr';
  }
}
